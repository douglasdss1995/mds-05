// String
var nome = "Douglas";
console.log(nome);

// Boolean
var ativo = true;
var inativo = false;
console.log(ativo);
console.log(inativo);

// Undefined
var indefinido;
console.log(indefinido);

// Número
var numero = 1;
console.log(numero);

// Número grande
var numero_grande = 90000000000000000n;
console.log(numero_grande);

// Object/Objeto
var objeto = {
    nome: "Maria",
    sobrenome: "Silva",
}
console.log(objeto);

// Array/Lista
var array = [1, 2, 3];
console.log(array);

// Date/Datas
