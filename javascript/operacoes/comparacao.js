// Igualdade
let igual = 5 == "5"; // igual é true
console.log("Igualdade: ", igual);

// Identidade
let identico = 5 === "5"; // identico é false
console.log("Identidade: ", identico);

// Desigualdade
let diferente = 5 != 6;
console.log("Desigualdade: ", diferente);

// Não identidade
let naoIdentico = 5 !== "5";
console.log("Identidade: ", naoIdentico);

// Maior
let maior = 10 > 5;
console.log("Maior: ", maior);

// Menor
let menor = 5 < 10;
console.log("Menor: ", menor);

// Maior ou igual a
let maiorOuIgualA = 10 >= 5;
console.log("Maior ou igual a: ", maiorOuIgualA);

// Menor ou igual a
let menorOuIgualA = 5 <= 10;
console.log("Menor ou igual a: ", menorOuIgualA);
