
// Declaração
let a;
let b;

// Atribuição simples
a = 10;
b = 10;
console.log('A:', a);
console.log('B:', b);

// Atribução com adição
b += a;
console.log('A:', a);
console.log('B:', b);

//Atribução com subtração
a -= b;
console.log('A:', a);
console.log('B:', b);

// Atribuição com divisão
b /= a;
console.log('A:', a);
console.log('B:', b);

// Atribuição com multiplicação
a *= b;
console.log('A:', a);
console.log('B:', b);
